package com.prokarma.ejercitacion.ej12;

public abstract class Vehiculo {

	protected int tarifa;
	protected int id;

	public int getTarifa() {
		return tarifa;
	}

	public void setTarifa(int tarifa) {
		this.tarifa = tarifa;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	
	
}
